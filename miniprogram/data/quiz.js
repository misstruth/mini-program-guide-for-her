function createQuestion(id, title, options) {
  return { id, title, options }
}

function createStyleQuestion(id, title, options) {
  return { id, title, options }
}

function opt(key, text, scoreKey) {
  return { key, text, scoreKey }
}

function styleOpt(key, text, scoreKey) {
  return { key, text, scoreKey }
}

function getTypeByKey(collection, key) {
  return collection.find((item) => item.key === key)
}

function buildRoleCatalog() {
  return MAIN_TYPES.map((mainType) => ({
    ...mainType,
    roles: STYLE_TYPES.map((styleType, index) => ({
      id: `${mainType.key}-${styleType.key}`,
      mainKey: mainType.key,
      styleKey: styleType.key,
      name: ROLE_NAME_MAP[mainType.key][index],
      mainLabel: mainType.label,
      styleLabel: styleType.label,
    })),
  }))
}

function resolveRole(mainKey, styleKey) {
  const styleIndex = STYLE_TYPES.findIndex((item) => item.key === styleKey)
  const mainType = getTypeByKey(MAIN_TYPES, mainKey)
  const styleType = getTypeByKey(STYLE_TYPES, styleKey)
  const roleName = ROLE_NAME_MAP[mainKey][styleIndex]

  return {
    id: `${mainKey}-${styleKey}`,
    name: roleName,
    mainKey,
    styleKey,
    mainLabel: mainType.label,
    styleLabel: styleType.label,
    summary: mainType.summary,
    tone: styleType.tone,
  }
}

function buildResultCopy(mainKey, styleKey, secondaryKey) {
  const role = resolveRole(mainKey, styleKey)
  const secondary = getTypeByKey(MAIN_TYPES, secondaryKey) || getTypeByKey(MAIN_TYPES, mainKey)
  const mainDetail = MAIN_TYPE_DETAILS[mainKey]
  const styleDetail = STYLE_DETAILS[styleKey]

  return {
    role,
    headline: `${role.name}`,
    subtitle: `${role.mainLabel} x ${role.styleLabel}`,
    verdict: `${mainDetail.aura}${role.styleLabel}的你，${styleDetail.flair}。`,
    strengths: [
      `你的主打法是 ${mainDetail.strengths.join('、')}。`,
      `你带着 ${role.styleLabel} 的手感做事，现场通常会先感受到你的 ${styleDetail.tags.join('和')}。`,
      `副人格偏 ${secondary.label}，说明你不只有一种做事电路，压力来时会自动切另一套打法。`,
    ],
    risks: [
      `你的常见翻车点是 ${mainDetail.risks.join('、')}。`,
      `当 ${role.styleLabel} 模式拉满时，你可能会把自己的优势推到过量使用。`,
      `如果团队没有给你足够反馈，你会倾向于自己默默接管更多责任。`,
    ],
    scenes: [
      `你最适合的局通常是：${mainDetail.scenes.join('、')}。`,
      `如果把你放在需要 ${role.mainLabel} 又允许 ${role.styleLabel} 发挥的岗位里，存在感会直接翻倍。`,
    ],
    roast: `黑话点评：${mainDetail.roast}`,
    tags: [...mainDetail.strengths.slice(0, 2), ...styleDetail.tags],
  }
}

